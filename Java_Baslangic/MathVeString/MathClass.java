package MathVeString;

public class MathClass {

	public static void main(String[] args) {
		
		// Absolute (mutlak) değer 
		System.out.println(Math.abs(-100));
		
		// Küp kök
		System.out.println(Math.cbrt(8));
		
		// Tavan tam sayıya yuvarlar.
		System.out.println(Math.ceil(1.0002));
		
		// taban tam sayıya yuvarlar
		System.out.println(Math.floor(1.999));
		
		// Max ve Min değeri verir.
		System.out.println(Math.max(1,54));
		
		// üs alma işlemi.
		System.out.println(Math.pow(2,3));
		
		// rastgele 0 ve 1 arasında bir değer verir.
		System.out.println(Math.random() * 100);
		
		// yakın tam sayıya yuvarlar.
		System.out.println(Math.round(1.22));
		
		// yakın double tam sayıya yuvarlar.
		System.out.println(Math.rint(1.22));
		
		// değerin işaretini verir.
		System.out.println(Math.signum(-100));
		
		// karekök alma işlemi.
		System.out.println(Math.sqrt(1.22));
				
	}

}
